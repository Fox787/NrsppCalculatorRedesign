                            by providing access to a ‘knowledge bank’ from a diverse network of organisations to given
                            them the resources to implement their own initiatives. The tools will help make the business
                            case for organisations shifting their safety focus from 'having' to safety to secure a
                            contract to 'wanting' to because it is simply good business.</p>
