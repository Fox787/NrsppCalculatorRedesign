                <div>
                <p>The National Road Safety Partnership Program (NRSPP) Total Road Incident Cost Calculator was designed
                    to provide organisations with an estimate of the direct and indirect costs associated with a road
                    incident.</p>
                <p>The calculator presents an estimate of total incident costs in simple terms, underlining how much
                    each incident could potentially cost an organisation in terms of lost revenue or service provision.
                    In so doing, it helps organisations understand the cost of road incidents relative to productivity,
                    profitability and service delivery. The calculator will help you articulate the additional
                    production or service delivery needed to recover the Total Cost of Risk.</p>

                <h2><strong>Who can use the calculator?</strong></h2>
                <p>The calculator recognises the differences in operations and business structure across different
                    sectors. To this end, tailored solutions and calculations are provided for the Manufacturing and
                    Services sector, State and Local Government sector, Health and Emergency Services and Other
                    Community Services and Not-for-Profit organisations.</p>
