                        <p>The National Road Safety Partnership Program (NRSPP) offers a collaborative network for
                            Australian organisations to build and implement effective road safety strategies in the
                            workplace.</p>
                        <p>The program offers organisations the resources to improve road safety that best fit their
                            individual operations and, at the same time, improve business productivity through less time
                            and money lost through safety incidents.</p>
